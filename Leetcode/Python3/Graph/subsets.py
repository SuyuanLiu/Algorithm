class Solution:
    def subsets(self, nums: List[int]) -> List[List[int]]:
        res = [[]]
        if not nums:
            return res
    
        for i in range(len(nums)):
            path = [[nums[i]]]
            for j in range(i+1, len(nums)):
                path = path + [n + [nums[j]] for n in path]
            res += path
        
        return res
